COMMENT ON COLUMN public.shops.contact_name IS '联系人';
COMMENT ON COLUMN public.shops.shop_type IS '门店类型（自营/加盟/共享/品牌联营等）';
COMMENT ON COLUMN public.shops.contact_phone IS '联系人电话';
COMMENT ON COLUMN public.shops.province IS '省';
COMMENT ON COLUMN public.shops.province_code IS '省级行政区划代码';
COMMENT ON COLUMN public.shops.city IS '市';
COMMENT ON COLUMN public.shops.city_code IS '市级行政区划代码';
COMMENT ON COLUMN public.shops.district IS '区/县';
COMMENT ON COLUMN public.shops.district_code IS '区县级行政区划代码';
COMMENT ON COLUMN public.shops.street IS '街道';
COMMENT ON COLUMN public.shops.street_code IS '街道级区划代码';
COMMENT ON COLUMN public.shops.address IS '详细地址';
COMMENT ON COLUMN public.shops.latitude IS '纬度';
COMMENT ON COLUMN public.shops.longitude IS '经度';
COMMENT ON COLUMN public.shops.geohash IS '地理位置 geohash';
COMMENT ON COLUMN public.shops.business_status IS '营业状态（营业、歇业等）';
COMMENT ON COLUMN public.shops.shop_images IS '店铺图片列表';
COMMENT ON COLUMN public.shops.tags IS '标签列表';
COMMENT ON COLUMN public.shops.description IS '门店描述';
COMMENT ON COLUMN public.shops.is_deleted IS '是否删除';
COMMENT ON COLUMN public.shops.created_at IS '创建时间，插入时自动赋值';
COMMENT ON COLUMN public.shops.updated_at IS '更新时间，更新时自动赋值';
COMMENT ON COLUMN public.shops.deleted_at IS '删除时间（软删时记录）';

