-- PostgreSQL 18 — 店铺信息 (shops)
-- Depends on: 00_moonite_touch_updated_at.sql, merchants.sql

CREATE TABLE public.shops (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  merchant_id bigint NOT NULL REFERENCES public.merchants (id) ON DELETE RESTRICT,
  shop_name varchar(255) NOT NULL DEFAULT '',
  contact_name varchar(128) NOT NULL DEFAULT '',
  shop_type varchar(32) NOT NULL DEFAULT '',
  contact_phone varchar(32) NOT NULL DEFAULT '',
  province varchar(64) NOT NULL DEFAULT '',
  province_code varchar(16) NOT NULL DEFAULT '',
  city varchar(64) NOT NULL DEFAULT '',
  city_code varchar(16) NOT NULL DEFAULT '',
  district varchar(64) NOT NULL DEFAULT '',
  district_code varchar(16) NOT NULL DEFAULT '',
  street varchar(255) NOT NULL DEFAULT '',
  street_code varchar(16) NOT NULL DEFAULT '',
  address varchar(255) NOT NULL DEFAULT '',
  latitude double precision NULL,
  longitude double precision NULL,
  geohash varchar(32) NOT NULL DEFAULT '',
  -- Optional PostGIS: add `location geometry(Point, 4326)` after `CREATE EXTENSION postgis`.
  business_status varchar(16) NOT NULL DEFAULT '',
  shop_images text[] NOT NULL DEFAULT ARRAY[]::text[],
  tags text[] NOT NULL DEFAULT ARRAY[]::text[],
  description text NOT NULL DEFAULT '',
  is_deleted boolean NOT NULL DEFAULT false,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  deleted_at timestamptz NULL
);

CREATE TRIGGER shops_touch_updated_at
  BEFORE UPDATE ON public.shops
  FOR EACH ROW
  EXECUTE FUNCTION public.moonite_touch_updated_at();

COMMENT ON TABLE public.shops IS '店铺信息';
